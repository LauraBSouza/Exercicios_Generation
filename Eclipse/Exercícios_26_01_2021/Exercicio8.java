package Familia44;

import java.util.Scanner;

/* O custo ao consumidor de um carro novo é a soma do custo de fábrica com a
percentagem do distribuidor e dos impostos (aplicados ao custo de fábrica).
Supondo que a percentagem do distribuidor seja de 28% e os impostos de 45%,
escrever um sistema que leia o custo de fábrica de um carro e escreva o custo ao
consumidor.  */

public class Exercicio8 {
	public static void main(String[] args)  {
		
		double cFabrica,custoConsumidor;
		
		Scanner leia = new Scanner(System.in);
		
		System.out.println("Escreva o custo de fábrica:");
		cFabrica = leia.nextDouble();
		
		custoConsumidor = (cFabrica + (cFabrica * 0.28) + (cFabrica * 0.45));
		System.out.println("Escreva o custo do consumidor é de R$ "+custoConsumidor);
		
		
	}

}
